#include<iostream>
using namespace std;

int main(){

    // Increment and 

    int x = 5;
    cout<<x<<endl;

    ++x; // Pre-increment
    cout<<x<<endl;

    x++; // Post increment
    cout<<x<<endl; 


    // Decrement

    cout<<x<<endl;

    --x; // Predcrement
    cout<<x<<endl;

    x--; // Post Decrement
    cout<<x<<endl; 
    return 0;
}